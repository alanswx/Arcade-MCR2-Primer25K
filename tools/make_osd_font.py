OUT = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                   "..", "src", "rtl", "osd_font.hex")


def main():
    lines = []
    for code in range(0x20, 0x60):
        rows = GLYPHS.get(chr(code), GLYPHS[' '])
        assert len(rows) == 7
        for r in rows:
            assert 0 <= r <= 0x1F, f"row {r:#x} of {chr(code)!r} too wide"
            lines.append(f"{r << 2:02X}")   # 5-bit pattern into bits 6..2
        lines.append("00")                  # row 7 blank
    with open(OUT, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"Wrote {os.path.normpath(OUT)} ({len(lines)} bytes)")


if __name__ == "__main__":
    main()
