"""Generate src/rtl/osd_font.hex - the 8x8 font ROM for osd.sv.

64 glyphs covering ASCII 0x20..0x5F (space, punctuation, digits, upper case),
8 bytes each = 512 lines of 2-digit hex. Glyph row byte: bit 7 = leftmost
pixel. The 5x7 patterns below sit in bits 6..2 with a blank row 7, giving
1-2 px of margin inside the cell.

Hand-drawn (classic 5x7 dot-matrix shapes) - our own artwork, safe to commit.

Run from the repo root:  python3 tools/make_osd_font.py
"""

